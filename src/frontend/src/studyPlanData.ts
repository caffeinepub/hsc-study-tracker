export const TOTAL_DAYS = 182;
export const START_DATE = new Date(2026, 2, 22); // March 22, 2026 (month is 0-indexed)

export function getTodayDayNumber(): number {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const start = new Date(START_DATE);
  start.setHours(0, 0, 0, 0);
  const diffMs = today.getTime() - start.getTime();
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));
  return Math.max(0, Math.min(diffDays, TOTAL_DAYS - 1));
}

export function getTasksForDay(dayIndex: number): string[] {
  if (dayIndex < 0 || dayIndex >= studyPlan.length) return [];
  const plan = studyPlan[dayIndex];
  return [plan.task1, plan.task2, plan.task3].filter((t) => t.trim() !== "");
}

export function getDayPhase(dayIndex: number): string {
  return dayIndex < 84
    ? "Basic To Admission Foundation Phase"
    : "Dedicated Test Paper + QB Solve Phase";
}
